# Variables 
#        =>In python there is no need to specify the data type of a variable at the time of  declaration

# Example :
a = 1
b = "Aromal"
print(a)
print(b)

# Output :
#         1
#         Aromal

# Input value from user and store it in a variable

# Example :
name = input("Enter your name :")
print("Hello "+name)

# Output :
#        Enter your name :aromal
#        Hello aromal

# Length of a string => len()  
#                    =>it counts the spaces between the words also

# Example :
str = input("Enter your name :")
length = len(str)
print(length)

# Output :
#         Enter your name :Aromal Sunil
#         12


